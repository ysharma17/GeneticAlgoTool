    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked
        // TODO add your handling code here:
        
        // Dialog for city names
    String input = JOptionPane.showInputDialog(null,"Place has been marked!" +
            "\nEnter the name of the city:");
    City city = new City(evt.getX(), evt.getY(), input);
    cities.add(city);
    Color color;
    if(cities.size() > 1)
        color = Color.RED;
    else
        color = Color.BLUE;

    paintCity(jLabel5.getGraphics(), evt.getX(), evt.getY(), color);    
    list1.add(city.name);
    if(list1.getItemCount() > 0) {
        jButton1.setEnabled(true);
        jButton2.setEnabled(true);
        jButton4.setEnabled(true);
    }//GEN-LAST:event_jLabel5MouseClicked
   }
    /**
 * Cleans the map of all points and routes
 */
private void clearMap() {
    jLabel5.paint(jLabel5.getGraphics());
}
/**
 * Records all the cities on the map
 */
private void repaintCities() {
    int i=0;
    Color color;
    for(City ort:cities) {
        if(i == 0)
            color = Color.BLUE;
        else
            color = Color.RED;
        paintCity(jLabel5.getGraphics(), ort.x, ort.y, color);
        i++;
    }        
}
public void drawConnections(Graphics g, City[] route) {
    g.setColor(Color.GREEN);
    for(int i=0; i < cities.size()-1; i++) {
        g.drawLine(route[i].x, route[i].y, route[i+1].x, route[i+1].y);
    }
    /* End to starting point */
    g.drawLine(route[cities.size()-1].x, route[cities.size()-1].y, 
                route[0].x, route[0].y);
}
/**
 * Draws a point at the given coordinate and...
 * @Graphics g
 * @int x
 * @int y
 */
private void paintCity(Graphics g, int x, int y, Color color) {
    g.setColor(color);
    g.fillOval(x-4, y-4, 8, 8);
}
