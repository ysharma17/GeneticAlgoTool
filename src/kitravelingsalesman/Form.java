private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    int numPop = Integer.parseInt(String.valueOf(jSpinner1.getValue()));
    City[] best;
    Route route = new Route(cities, numPop);
    route.setGeneration(Integer.parseInt(String.valueOf(jSpinner2.getValue())));
    route.setMutationsrate(Integer.parseInt(String.valueOf(jSpinner3.getValue())));
    best = route.travelingSalesman();
    clearMap();
    repaintCities();
    drawConnections(jLabel5.getGraphics(), best);
    jLabel9.setVisible(true);
    jLabel10.setVisible(true);
    jtotDistLabel.setText((float)(Math.round((route.getTotalDistance()*100))/100f) + " km");
    for(int i=0; i < cities.size(); i++) {
        System.out.print(best[i].name + " -> ");
    }
}//GEN-LAST:event_jButton3ActionPerformed

